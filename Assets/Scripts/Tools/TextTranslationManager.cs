    public static string GetText(TextCollection textCollection, int textID, int language)
    {
        if (singleton == null)
        {
            Debug.LogError("Singleton is null! Aborting.");
            return "";
        }
        return singleton._GetText(textCollection, textID, language);
    }

 //   public enum TextCollection { startMenu, pause, common, mapLevels, itemPrompts, npc, cutscene, levelTitles, plazaMisc, textPrompts };

    string _GetText(TextCollection textCollection, int textID, int language)
    {
        string stringToReturn = "";

        if (textCollection == TextCollection.startMenu && textID < startMenuTexts.Length)
            stringToReturn = startMenuTexts[textID, language];

        if (textCollection == TextCollection.pause && textID < pauseMenuTexts.Length)
            stringToReturn = pauseMenuTexts[textID, language];

        if (textCollection == TextCollection.common && textID < commonTexts.Length)
            stringToReturn = commonTexts[textID, language];

        if (textCollection == TextCollection.mapLevels && textID < mapLevelNamesTexts.Length)
            stringToReturn = mapLevelNamesTexts[textID, language];

        if (textCollection == TextCollection.itemPrompts && textID < itemPromptTexts.Length)
            stringToReturn = itemPromptTexts[textID, language];

        if (textCollection == TextCollection.npc && textID < npcTexts.Length)
            stringToReturn = npcTexts[textID, language];

        if (textCollection == TextCollection.cutscene && textID < cutsceneTexts.Length)
            stringToReturn = cutsceneTexts[textID, language];

        if (textCollection == TextCollection.levelTitles && textID < levelTitlesTexts.Length)
            stringToReturn = levelTitlesTexts[textID, language];

        if (textCollection == TextCollection.plazaMisc && textID < plazaMiscTexts.Length)
            stringToReturn = plazaMiscTexts[textID, language];

        if (textCollection == TextCollection.textPrompts && textID < textPrompts.Length)
            stringToReturn = textPrompts[textID, language];

        if (textCollection == TextCollection.tutorials && textID < tutorials.Length)
            stringToReturn = tutorials[textID, language];

        if (stringToReturn == "")
            return "OUT OF ARRAY";
        else
        {
            if (language == 11)
                stringToReturn = ReverseString(stringToReturn);

            return stringToReturn;
        }
    }


    public static string ReverseString(string s)
    {
        char[] charArray = s.ToCharArray();
        Array.Reverse(charArray);
        return new string(charArray);
    }
